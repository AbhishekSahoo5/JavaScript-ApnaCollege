//Arithmetic Operators
let a=5;
let b=2;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(a**b);
console.log("----------------------------------");


//Unary Operator
let c=5;
console.log(c++); //-->post increment
console.log(++c); //-->pre increment
console.log(c--); //-->post decrement
console.log(--c); //-->pre decrement
console.log("----------------------------------");

//Assignment Operators
// eg. =,  +=, -=, *=, %=, **=


//Comparision Operator
/*
==(equal to)
!=(not equal to)
===(equal to & type)
!==(not equal & type)
*/
let x=4;
let y="4";
console.log(x==y);
console.log(x===y);

console.log(x!=y);
console.log(x!==y);
console.log("--------------------------------");

//Logical Operators
/*
1. Logical AND
2. Logical OR
3. Logical NOT
*/
let u=10;
let v=20;
let cond1=u<v;
let cond2=u==v;
console.log(cond1&&cond2);
console.log(cond1||cond2);






